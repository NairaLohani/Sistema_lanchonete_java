    private void jTable1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTable1KeyReleased
        // TODO add your handling code here:
        if(evt.getKeyCode() == KeyEvent.VK_UP || evt.getKeyCode() == KeyEvent.VK_DOWN){
            rowIndex = jTable1.getSelectedRow();
             jtxt_id_cliente.setText(model.getValueAt(rowIndex, 0).toString());
        jtxt_nome.setText(model.getValueAt(rowIndex, 1).toString());
        jtxt_email.setText(model.getValueAt(rowIndex, 2).toString());
        jtxt_telefone.setText(model.getValueAt(rowIndex, 3).toString());
        jTextArea_end.setText(model.getValueAt(rowIndex, 4).toString());
        }
        
    }//GEN-LAST:event_jTable1KeyReleased

