    public boolean verificText(){
        if(jtxt_nome.getText().equals("")|| jtxt_id_cliente.getText().equals("") || jtxt_email.getText().equals("")|| jtxt_telefone.getText().equals("") || jTextArea_end.getText().equals("") ){
            JOptionPane.showMessageDialog(null, "Um ou mais espaços vazios!");
            return false;
        }
        else{
                return true;
        }
        
        
    }
    
    private void jButton_edit_clienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_edit_clienteActionPerformed

        String nome = jtxt_nome.getText();
        String email = jtxt_email.getText();
        String telefone = jtxt_telefone.getText();
        String endereco = jTextArea_end.getText();
        int id =Integer.valueOf(jtxt_id_cliente.getText());
        
        if(verificText()){
            ClienteDAO cli = new ClienteDAO();
            cli.InserirAtualizarDeletarCliente('u', id, nome, email, telefone, endereco);
            jTable1.setModel(new DefaultTableModel(null, new Object[]{"Id","Nome","E-mail","Telefone","Endereço"}));
       cli.peencherTabelaCliente(Crudcliente.jTable1, "");
            //Principal.lblqtd_clientes.setText("Quantidade de Clientes = "+ Integer.toString(Minhafuncao.contarDados("cliente")));
        }
        
    }//GEN-LAST:event_jButton_edit_clienteActionPerformed
